export const getTemplatesByCategory = (category: string): HabitTemplate[] => {
  return habitTemplates.filter(t => t.category === category);
};

export const searchTemplates = (query: string): HabitTemplate[] => {
  const lowerQuery = query.toLowerCase();
  return habitTemplates.filter(
    t => t.name.toLowerCase().includes(lowerQuery) ||
         t.description.toLowerCase().includes(lowerQuery)
  );
};
